	public static long runA(String input) {
		List<PuzzlePiece> PuzzlePieces = Arrays.stream(input.split("\n\n"))
				.map(PuzzlePiece::new)
				.collect(Collectors.toList());
		Map<String, List<String>> sidesMap = PuzzlePieces.parallelStream().map(p -> p.allSideOrientations)
				.flatMap(Collection::parallelStream)
				.collect(Collectors.groupingBy(s -> s));
		PuzzlePieces.stream().forEach(p -> p.setOccurencesOfSides(sidesMap));
		return PuzzlePieces.stream().filter(p -> p.isCorner()).mapToLong(p -> p.name).reduce((a, b) -> a * b).getAsLong();
	}

	public static int runB(String input) {
		input = input.replace(".", "_");
		List<PuzzlePiece> puzzlePieces = Arrays.stream(input.split("\n\n"))
				.map(PuzzlePiece::new)
				.collect(Collectors.toList());
		Map<String, List<String>> sidesMap = puzzlePieces.parallelStream().map(p -> p.allSideOrientations)
				.flatMap(Collection::parallelStream)
				.collect(Collectors.groupingBy(s -> s));
		puzzlePieces.stream().forEach(p -> p.setOccurencesOfSides(sidesMap));
		PuzzlePiece corner = puzzlePieces.stream().filter(PuzzlePiece::isCorner).findAny().get();

		int sqrt = (int) Math.sqrt(puzzlePieces.size());
		Map<Integer, Map<Integer, PuzzlePiece>> puzzle = new HashMap<>();
		IntStream.range(0, sqrt)
				.forEach(row -> {
					HashMap<Integer, PuzzlePiece> tempMap = new HashMap<>();
					puzzle.put(row, tempMap);
					// place first corner piece
					if (row == 0) {
						corner.turnCorner(puzzlePieces);
						puzzlePieces.remove(corner);
						tempMap.put(0, corner);
					} else {
						// find puzzlepiece below
						PuzzlePiece pieceAbove = puzzle.get(row -1).get(0);
						List<PuzzlePiece> pieces = puzzlePieces.stream().filter(p -> p.fitsBelow(pieceAbove)).collect(Collectors.toList());
						puzzlePieces.remove(pieces.get(0));
						tempMap.put(0, pieces.get(0));
					}

					IntStream.range(1, sqrt).forEach(column -> {
						List<PuzzlePiece> pieces = puzzlePieces.stream().filter(p -> p.fitsRightAndOrientate(tempMap.get(column - 1))).collect(Collectors.toList());
						puzzlePieces.remove(pieces.get(0));
						tempMap.put(column, pieces.get(0));
					});
			});

		String radarImage = puzzle.values().stream().map(
				line ->
						IntStream.range(0, 8).mapToObj(i ->
								line.values().stream().map(l -> l.getLineWithoutBorder(i))
										.collect(Collectors.joining("")))
								.collect(Collectors.joining("\n")))
				.collect(Collectors.joining("\n"));

//		System.out.println(radarImage);

		int count =0;
		// Try the 4 directions, flip, than try 4 directions again.
		for(int i = 0; i < 4; i++) {
			count = countSeaMonsters(radarImage, '#');
			radarImage = StringHelper.rotateBlockLeft(radarImage);
		}
		radarImage = StringHelper.flipBlock(radarImage);
		for(int i = 0; i < 4; i++) {
			count = countSeaMonsters(radarImage, '#');
			radarImage = StringHelper.rotateBlockLeft(radarImage);
		}

		return getNumWaves(radarImage) - getNumWaves(nessieFull) * count;
	}

	static int getNumWaves(String radarImage) {
		int count = 0;

		for (int i = 0; i < radarImage.length(); i++) {
			if (radarImage.charAt(i) == '#') {
				count++;
			}
		}
		return count;
	}

	static String nessieFull =
			"                  # \n" +
			"#    ##    ##    ###\n" +
			" #  #  #  #  #  #   ";
	static String[] nessie = nessieFull.split("\n");

	public static int countSeaMonsters(String radarImage, char c) {
		String[] splitRadarImage = radarImage.split("\n");
		return (int) IntStream.range(nessie[0].length() - 2 , splitRadarImage.length - 2) // rows
			.mapToObj(x -> IntStream.range(0, splitRadarImage.length - nessie.length)
					.filter(y -> isNessieAt(splitRadarImage, x, y, c))
					.mapToObj(a -> true)
					.collect(Collectors.toList()))
				.flatMap(Collection::stream)
				.count();
	}

	public static boolean isNessieAt(String[] radarImageLines, int x, int y, char c) {
		if(radarImageLines[y].charAt(x) != c)
			return false;

		// Does second line nessie match up
		boolean b = IntStream.range(0, nessie[1].length())
				.allMatch(i -> {
					if (nessie[1].charAt(i) == '#') {
						return radarImageLines[y + 1].charAt(x - (nessie[1].length() - 2) + i) == c;
					}
					return true;
				});
		if(!b) return false;

		// Does third line nessie match up
		boolean b1 = IntStream.range(0, nessie[2].length())
				.allMatch(i -> {
					if (nessie[2].charAt(i) == '#') {
						return radarImageLines[y + 2].charAt(x - (nessie[2].length() - 2) + i) == c;
					}
					return true;
				});
		return b1;
	}

	private static class PuzzlePiece {
		Long name;
		List<String> sides = new ArrayList<>(); // side 0 = top, 1 = right, 2 = bottom, 3 = left
		List<Integer> occurences = new ArrayList<>();
		List<String> flippedSides = new ArrayList<>();
		List<String> allSideOrientations = new ArrayList<>();
		String block;
		String borderLessBlock;


		public String getLineWithoutBorder(int i) {
			if (borderLessBlock == null) {
				borderLessBlock = getBlockWithoutBorder();
			}
			return borderLessBlock.split("\n")[i];
		}

		public String getBlockWithoutBorder() {
			String[] split = block.split("\n");
			return IntStream.range(1, split.length - 1)
					.mapToObj(i -> split[i].substring(1, split[i].length() -1))
			.collect(Collectors.joining("\n"));
		}

		public PuzzlePiece(String string) {
			String[] split = string.split("\n");
			name = Long.parseLong(split[0].substring(5, split[0].length()-1));
			this.block = string.substring(string.indexOf("\n") +1);
			if(name == 1591) {
				flipBlock();
			}
			StringBuilder left = new StringBuilder();
			StringBuilder right = new StringBuilder();
			IntStream.range(1, split.length).forEach(i -> {
					left.append(split[i].charAt(0));
					right.append(split[i].charAt(split[i].length()-1));
				}
			);
			resetSides();
		}

		void resetSides() {
			sides = new ArrayList<>();
			String[] split = block.split("\n");
			StringBuilder left = new StringBuilder();
			StringBuilder right = new StringBuilder();

			IntStream.range(0, split.length).forEach(i -> {
						left.append(split[i].charAt(0));
						right.append(split[i].charAt(split[i].length()-1));
					}
			);
			sides.add(split[0]);
			sides.add(right.toString());
			sides.add(split[split.length - 1]);
			sides.add(left.toString());

			flippedSides = new ArrayList<>();
			sides.stream().forEach(s -> flippedSides.add(StringHelper.flipString(s)));

			allSideOrientations = new ArrayList<>();
			allSideOrientations.addAll(flippedSides);
			allSideOrientations.addAll(sides);
		}

		void turnCorner(List<PuzzlePiece> pieces) {
			if(occurences.get(2) == 1 && occurences.get(3) == 1) {
				rotateBlock1Left();
			}

			if(occurences.get(1) == 1 && occurences.get(2) == 1) {
				rotateBlock1Left();
				rotateBlock1Left();
			}

			if(occurences.get(0) == 1 && occurences.get(1) == 1) {
				rotateBlock1Left();
				rotateBlock1Left();
				rotateBlock1Left();
			}

			// Unclear why, just comment out some or all of the lines until it works.
//			rotateBlock1Left();
//			rotateBlock1Left();
//			rotateBlock1Left();
		}

		boolean fitsRightAndOrientate(PuzzlePiece piece) {
			String matchingSide = piece.sides.get(1); // right side of block before
			if(!allSideOrientations.contains(matchingSide)) {
				return false;
			}

			for(int i = 0; i < 5; i++) {
				if(sides.get(3).equals(matchingSide)) {
					return true;
				}
				rotateBlock1Left();
			}
			flipBlock();
			for(int i = 0; i < 5; i++) {
				if(sides.get(3).equals(matchingSide)) {
					return true;
				}
				rotateBlock1Left();
			}
			return true; // shouldn't get here.
		}

		void rotateBlock1Left() {
			block = StringHelper.rotateBlockLeft(block);
			// change the order of the sides in the sides list to match the new orientation.
			resetSides();
		}

		void flipBlock() {
			block = StringHelper.flipBlock(block);
			resetSides();
		}

		boolean fitsBelow(PuzzlePiece piece) {
			String matchingSide = piece.sides.get(2); // bottom side of block before
			if(!allSideOrientations.contains(matchingSide)) {
				return false;
			}

			for(int i = 0; i < 5; i++) {
				if(sides.get(0).equals(matchingSide)) {
					return true;
				}
				rotateBlock1Left();
			}
			flipBlock();
			for(int i = 0; i < 5; i++) {
				if(sides.get(0).equals(matchingSide)) {
					return true;
				}
				rotateBlock1Left();
			}
			return true;// shouldnt get here
		}

		boolean isCorner() {
			return occurences.stream().filter(i -> i == 1)
			.count() == 2;
		}


		void setOccurencesOfSides(Map<String, List<String>> sidesMap) {
			sides.stream().forEach(side -> occurences.add(sidesMap.get(side).size()));
		}
	}

